fun main(args: Array<String>){
    var five: Char = '5'

    val strFive : String = five.toString()
    println(strFive)
    val bFive : Byte = five.toByte()
    println(bFive)
    val shFive : Short = five.toShort()
    println(shFive)
    val iFive : Int = five.toInt()
    println(iFive)
    val dFive : Double = five.toDouble()
    println(dFive)
    val fFive : Float = five.toFloat()
    println(fFive)
    val lFive : Long = five.toLong()
    println(lFive)

}
